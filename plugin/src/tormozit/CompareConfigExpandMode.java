package tormozit;


public enum CompareConfigExpandMode
{
    /** Развернуть до верхних объектов (без погружения в свойства). */
    toObject,

    /** Развернуть до изменённых (все, кроме добавленных/удалённых). */
    toBothElement,

    /** Развернуть до помеченных (узлы с установленным чекбоксом). */
    toMarked
}
